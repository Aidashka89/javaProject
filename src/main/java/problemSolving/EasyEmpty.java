import java.util.Scanner;

public class EasyEmpty {
    public static void main(String[] args) {
        Scanner abc = new Scanner(System.in);
        String i = abc.next();
        System.out.println("Enter a word");

        if (i.isEmpty()){
            System.out.println("Empty");
        }else {
            System.out.println("Not empty");
        }

        //==================================================//












    }




}
